package exercicio;

public class CalculaMedia {
    public static void main(String[] args) {
    	
        double nota1 = 7.5;
        double nota2 = 8.0;
        double nota3 = 9.2;
        double nota4 = 6.8;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;


        System.out.println("A média das notas é: " + media);
    }
}